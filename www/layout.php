<h1>Передача параметров. Шаблонизация</h1>
<h2>.htaccess и диспетчер доступа</h2>
<p>
    По требованиям безопасности (сертифицированным) ПО должно
    иметь диспетчер доступа - точку прохождения всех запросов. 
    РНР усложняет это требование, т.к. формально каждый файл 
    является "точкой входа". 
</p>
<p>
    Одно из решений - использовать модуль обработки запросов
    Apache. Его инструкции задаются в специальном файле .htaccess
</p>
